def count_positive_value(a,b,c):
    x = 0
    if a > 0:
        x += 1
    if b > 0:
        x += 1
    if c > 0:
        x += 1
    print("Result positive", x)
def count_negative_value(a,b,c):
    x = 0
    if a < 0:
        x += 1
    if b < 0:
        x += 1
    if c < 0:
        x += 1
    print("Result negative", x)
